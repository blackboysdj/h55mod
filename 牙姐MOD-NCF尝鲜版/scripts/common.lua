function getCreatureQuadrant(creature_target)
    local creature_pos_x, creature_pos_y = GetUnitPosition(creature_target);
    local i_battlesize = getBattleSize() + 1;
    local i_quadrant = 1;
    local target_pos_x = -1;
    local target_pos_y = -1;
    if creature_pos_x <= List_Pos_Center[i_battlesize][4][1] and creature_pos_y >= List_Pos_Center[i_battlesize][4][2] then
        i_quadrant = 4;
        target_pos_x = List_Pos_Center[i_battlesize][4][1];
        target_pos_y = List_Pos_Center[i_battlesize][4][2];
    end;
    if creature_pos_x <= List_Pos_Center[i_battlesize][3][1] and creature_pos_y <= List_Pos_Center[i_battlesize][3][2] then
        i_quadrant = 3;
        target_pos_x = List_Pos_Center[i_battlesize][3][1];
        target_pos_y = List_Pos_Center[i_battlesize][3][2];
    end;
    if creature_pos_x >= List_Pos_Center[i_battlesize][2][1] and creature_pos_y <= List_Pos_Center[i_battlesize][2][2] then
        i_quadrant = 2;
        target_pos_x = List_Pos_Center[i_battlesize][2][1];
        target_pos_y = List_Pos_Center[i_battlesize][2][2];
    end;
    if creature_pos_x >= List_Pos_Center[i_battlesize][1][1] and creature_pos_y >= List_Pos_Center[i_battlesize][1][2] then
        i_quadrant = 1;
        target_pos_x = List_Pos_Center[i_battlesize][1][1];
        target_pos_y = List_Pos_Center[i_battlesize][1][2];
    end;
    return i_quadrant, target_pos_x, target_pos_y;
end;

