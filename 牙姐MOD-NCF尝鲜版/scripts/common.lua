function geneUnitStatus(strUnitName, iType)
    if IsCombatUnit(strUnitName) == nil then
        return nil;
    end;
    local itemUnit = {};
    itemUnit['iListenType'] = iType;
    itemUnit['iSide'] = GetUnitSide(strUnitName);
    itemUnit['iUnitCategory'] = GetUnitType(strUnitName);
    itemUnit['strUnitName'] = strUnitName;
    if itemUnit['iUnitCategory'] == ENUM_CATEGORY.HERO then
        itemUnit['iUnitType'] = GetHeroName(strUnitName);
        itemUnit['iPositionX'], itemUnit['iPositionY'] = GetUnitPosition(strUnitName);
        itemUnit['iMana'] = GetUnitManaPoints(strUnitName);
        itemUnit['iMaxMana'] = GetUnitMaxManaPoints(strUnitName);
    elseif itemUnit['iUnitCategory'] == ENUM_CATEGORY.CREATURE then
        itemUnit['iUnitType'] = GetCreatureType(strUnitName);
        itemUnit['iUnitNumber'] = GetCreatureNumber(strUnitName);
        itemUnit['iPositionX'], itemUnit['iPositionY'] = GetUnitPosition(strUnitName);
        itemUnit['iMana'] = GetUnitManaPoints(strUnitName);
        itemUnit['iMaxMana'] = GetUnitMaxManaPoints(strUnitName);
        if index(List_DemonRage_Creatures, itemUnit['iUnitType']) > -1 and GetHero(itemUnit['iSide']) ~= nil and index(H55SMOD_StrongholdHeroes, GetHeroName(GetHero(itemUnit['iSide']))) > -1 then
            itemUnit['iRagePoints'] = GetRagePoints(strUnitName);
            itemUnit['iRageLevel'] = GetRageLevel(strUnitName);
        end;
    elseif itemUnit['iUnitCategory'] == ENUM_CATEGORY.WARMACHINE then
        itemUnit['iUnitType'] = GetWarMachineType(strUnitName);
        itemUnit['iPositionX'], itemUnit['iPositionY'] = GetUnitPosition(strUnitName);
    elseif itemUnit['iUnitCategory'] == ENUM_CATEGORY.BUILDING then
        itemUnit['iUnitType'] = GetBuildingType(strUnitName);
        itemUnit['iPositionX'], itemUnit['iPositionY'] = GetUnitPosition(strUnitName);
    elseif itemUnit['iUnitCategory'] == ENUM_CATEGORY.SPELLSPAWN then
        -- itemUnit['iUnitType'] = GetSpellSpawnType(strUnitName);
        itemUnit['iPositionX'], itemUnit['iPositionY'] = GetUnitPosition(strUnitName);
    end;
    return itemUnit;
end;

function extendCreaturePosition(itemCreature)
    local listCreaturePosition = {};
    listCreaturePosition[0] = {["x"] = itemCreature["iPositionX"], ["y"] = itemCreature["iPositionY"]};
    local lenCreaturePosition = 1;
    if contains(List_Large_Creatures, itemCreature["iUnitType"]) ~= nil then
        listCreaturePosition[1] = {["x"] = itemCreature["iPositionX"], ["y"] = itemCreature["iPositionY"] - 1};
        listCreaturePosition[2] = {["x"] = itemCreature["iPositionX"] - 1 , ["y"] = itemCreature["iPositionY"] - 1};
        listCreaturePosition[3] = {["x"] = itemCreature["iPositionX"] - 1, ["y"] = itemCreature["iPositionY"]};
        lenCreaturePosition = 4;
    end;
    itemCreature["listCreaturePosition"] = listCreaturePosition;
    itemCreature["lenCreaturePosition"] = lenCreaturePosition;
    return itemCreature;
end;

function matchChain(itemCreatureTarget, itemCreatureBeChained)
    itemCreatureTarget = extendCreaturePosition(itemCreatureTarget);
    itemCreatureBeChained = extendCreaturePosition(itemCreatureBeChained);
    for iIndexPositionTarget = 0, itemCreatureTarget["lenCreaturePosition"] - 1 do
        for iIndexPositionBeChain = 0, itemCreatureBeChained["lenCreaturePosition"] - 1 do
            if itemCreatureTarget["listCreaturePosition"][iIndexPositionTarget]["x"] - itemCreatureBeChained["listCreaturePosition"][iIndexPositionBeChain]["x"] >= -1
                    and itemCreatureTarget["listCreaturePosition"][iIndexPositionTarget]["x"] - itemCreatureBeChained["listCreaturePosition"][iIndexPositionBeChain]["x"] <= 1
                    and itemCreatureTarget["listCreaturePosition"][iIndexPositionTarget]["y"] - itemCreatureBeChained["listCreaturePosition"][iIndexPositionBeChain]["y"] >= -1
                    and itemCreatureTarget["listCreaturePosition"][iIndexPositionTarget]["y"] - itemCreatureBeChained["listCreaturePosition"][iIndexPositionBeChain]["y"] <= 1 then
                return 1;
            end;
        end;
    end;
    return nil;
end;

function equal(itemUnit, itemUnitLast)
    if itemUnit['iListenType'] == itemUnitLast['iListenType']
        and itemUnit['iSide'] == itemUnitLast['iSide']
        and itemUnit['iUnitCategory'] == itemUnitLast['iUnitCategory']
        and itemUnit['strUnitName'] == itemUnitLast['strUnitName'] then
        if itemUnit['iUnitCategory'] == ENUM_CATEGORY.HERO then
            if itemUnit['iUnitType'] == itemUnitLast['iUnitType']
                and itemUnit['iPositionX'] == itemUnitLast['iPositionX']
                and itemUnit['iPositionY'] == itemUnitLast['iPositionY']
                and itemUnit['iMana'] >= itemUnitLast['iMana']
                and itemUnit['iMaxMana'] == itemUnitLast['iMaxMana'] then
                return 1;
            end;
        elseif itemUnit['iUnitCategory'] == ENUM_CATEGORY.CREATURE then
            if itemUnit['iUnitType'] == itemUnitLast['iUnitType']
                -- and itemUnit['iUnitNumber'] == itemUnitLast['iUnitNumber']
                and itemUnit['iPositionX'] == itemUnitLast['iPositionX']
                and itemUnit['iPositionY'] == itemUnitLast['iPositionY']
                and itemUnit['iMana'] >= itemUnitLast['iMana']
                and itemUnit['iMaxMana'] == itemUnitLast['iMaxMana'] then
                return 1;
            end;
        elseif itemUnit['iUnitCategory'] == ENUM_CATEGORY.WARMACHINE then
            if itemUnit['iUnitType'] == itemUnitLast['iUnitType']
                and itemUnit['iPositionX'] == itemUnitLast['iPositionX']
                and itemUnit['iPositionY'] == itemUnitLast['iPositionY'] then
                return 1;
            end;
        elseif itemUnit['iUnitCategory'] == ENUM_CATEGORY.BUILDING then
            if itemUnit['iUnitType'] == itemUnitLast['iUnitType']
                and itemUnit['iPositionX'] == itemUnitLast['iPositionX']
                and itemUnit['iPositionY'] == itemUnitLast['iPositionY'] then
                return 1;
            end;
        elseif itemUnit['iUnitCategory'] == ENUM_CATEGORY.SPELLSPAWN then
            if itemUnit['iPositionX'] == itemUnitLast['iPositionX']
                and itemUnit['iPositionY'] == itemUnitLast['iPositionY'] then
                return 1;
            end;
        end;
    end;
    return 0;
end;

