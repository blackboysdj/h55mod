function length(array)
    local count = 0
    for index, element in array do
        count = count + 1
    end
    return count
end

function contains(array, sample)
    for index, element in array do
        if (element == sample) then
            return not nil;
        end;
    end;
    return nil;
end;

function index(array, sample)
    for index, element in array do
        if (element == sample) then
            return index;
        end
    end
    return -1;
end;

